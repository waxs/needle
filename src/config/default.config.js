/** ----------------------------------------
    Model
 ---------------------------------------- */

let model = [];

/** ----------------------------------------
     Create Constructor
 ---------------------------------------- */

const setDefaults = (data, trail) => {

    /** ----------------------------------------
        Set Model
     ---------------------------------------- */

    model = model.length > 0 ? model : data;

    /** ----------------------------------------
        Configuration
     ---------------------------------------- */

    const defaults = [

        /** ----------------------------------------
             Amount
         ---------------------------------------- */

        {
            name: 'amount',
            value: data.length || 0
        },

        /** ----------------------------------------
            Calc
         ---------------------------------------- */

        {
            name: 'calc',
            value: 0
        },

        /** ----------------------------------------
            Chunks
         ---------------------------------------- */

        {
            name: 'chunks',
            value: {}
        },

        /** ----------------------------------------
             Content
         ---------------------------------------- */

        {
            name: 'content',
            value: {}
        },

        /** ----------------------------------------
            Data
         ---------------------------------------- */

        {
            name: 'data',
            value: data
        },

        /** ----------------------------------------
             Info
         ---------------------------------------- */

        {
            name: 'info',
            value: {
                set: model.length,
                length: data.length
            }
        },

        /** ----------------------------------------
             Trail
         ---------------------------------------- */

        {
            name: 'trail',
            value: trail || { exe: [], data: [], prev: [] }
        }


    ];

    return defaults;
};

/** ----------------------------------------
    Exports
 ---------------------------------------- */

export default setDefaults;